package com.tydic.common.bean;

/**
 * 3.0 渠道视图角色类型
 * @author Administrator
 *
 */
public enum RoleType {
	PROVINCE("province", "全省-省级泛渠道角色"),
	CITY("city", "全省-市级泛渠道角色"),
	AREA("area", "全省-区县泛渠道角色"),
	GENERAL("general", "全省-普通泛渠道角色"),
	OTHER("other","非泛渠道角色-其他角色");
	
	private String key;
	private String value;
	
	private RoleType(String key, String value) {
		this.key = key;
		this.value = value;
	}
	
	public void setKey(String key) {
		this.key = key;
	}
	
	public String getKey() {
		return key;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
	
	public String getValue() {
		return value;
	}
}
